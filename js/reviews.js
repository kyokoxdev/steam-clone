// Helper function to get reviews for a specific game
const getGameReviews = (gameId) => {
    return reviewData.filter(review => review.gameId === gameId);
};

// Helper function to get reviews from a specific user
const getUserReviews = (userId) => {
    return reviewData.filter(review => review.userId === userId);
};

// Helper function to calculate the average rating for a game
const getAverageRating = (gameId) => {
    const gameReviews = getGameReviews(gameId);
    if (gameReviews.length === 0) return 0;
    
    const sum = gameReviews.reduce((total, review) => total + review.rating, 0);
    return sum / gameReviews.length;
};

